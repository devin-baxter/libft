#include "libft.h"

int	ft_sqrt(int n)
{
	int i;
	int sq;

	i = 1;
	sq = 1;
	if (n == 0)
		return (0);
	while (i * i < n)
		i++;
	if (n % i == 0)
		return (i);
	else
		return (0);
}
